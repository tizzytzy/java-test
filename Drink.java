

public class Drink {

    private String kind;
    private String cupType;


    public Drink(){}



    public Drink(String kind){

        this.kind = kind;


    }

    public String getKind() {
        return kind;
    }

    public void setKind(String kind) {
        this.kind = kind;
    }

    public String getCupType() {
        return cupType;
    }

    public void setCupType(String cupType) {
        this.cupType = cupType;
    }




}
